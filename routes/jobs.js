/** GET /  =>
 *   { jobs: [ { title, salary, equity, company_handle }, ...] }
 *
 * Can filter on provided search filters:
 * - title (will find case-insensitive, partial matches)
 * - minSalary
 * - hasEquity
 *
 * Authorization required: none
 */

router.get("/", async function (req, res, next) {
  try {
    // check validation of filters
    const result = jsonschema.validate(req.query, jobFilterSchema);
    if (!result.valid) {
      // pass validation errors to error handler
      // each error is mapped in the stack
      let listOfErrors = result.errors.map(error => error.stack);
      throw new BadRequestError(listOfErrors);
    }

    const { title, minSalary, hasEquity } = req.query;

    const jobs = await Job.findAll({ title, minSalary, hasEquity });
    return res.json({ jobs });
  } catch (err) {
    return next(err);
  }
});

/** GET /[id]  =>  { job }
 *
 *  Job is { id }
 *   where jobs is [{ id, title, salary, equity }, ...]
 *
 * Authorization required: none
 */

router.get("/:id", async function (req, res, next) {
  try {
    const job = await Job.get(req.params.id);
    return res.json({ job });
  } catch (err) {
    return next(err);
  }
});

